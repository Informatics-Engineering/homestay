<p align="justify">Curug ini kelihatan bagian atasnya dari Pantai Palangpang. Terletak juga di Desa Ciwaru, di aliran Sungai Cimarinjung. Tingginya lebih dari 50 m. Oleh orang lokal disebut juga Curug Goong karena pada hari tertentu terdengar suara gong. Menurut legenda, ada gong yang tersembunyi di curug tersebut, peninggalan keraton alam gaib. Batunya adalah batuan sedimen berjenis batu pasir tufan dan breksi. Di atasnya di aliran yang sama ada Curug Nyelempet dan Curug Dogdog. Ketiganya terbentuk karena proses tektonik. Air curug ini dipakai untuk mengairi persawahan di bawahnya.
Dari Pantai Palangpang berkendara ke sini hanya 5 menit melewati pemandangan persawahan dan perbukitan yang aduhai. Saat MyTrip lewat tampak pembangunan jalan masih berlangsung, akses baru ke arah Loji di Palabuhan Ratu melewati Puncak Darma. Dari tempat parkir jalan kaki ke area curug treknya datar dan sudah dikonblok, hanya 5 menit sudah sampai.</p>
