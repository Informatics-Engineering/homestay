<p align="justify">Pantai Minajaya mirip seperti Pantai Tenda Biru. Jika air laut surut, kita bisa melihat ibu-ibu pencari jukut hejo (rumput hijau) di pinggiran ombak dan bapak-bapaknya mencari udang lobster. Di sini, kita bisa membeli ikan atau udang tangkapan nelayan yang masih segar untuk dimasak dan makan di saung-saung pinggir pantai. Lagi-lagi saya tidak menemukan tempat sampah di area pantai.</p>
