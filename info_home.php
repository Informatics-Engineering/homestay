<p align="justify"> Titik pandang Panenjoan berada di pinggir jalan, Desa Taman Jaya, dengan ketinggian 300-an mdpl. Lokasinya sudah dibuat rapi dengan pagar pembatas di tepi tebing, menara-menara maupun teras-teras pandang, serta ada tulisan Panenjoan. Amfiteater alam raksasa Ciletuh dapat terlihat utuh dari sini. Dinding tebing setengah lingkaran yang menghadap Laut Selatan seakan melindungi persawahan, perbukitan, serta perkampungan warga di bawahnya. Tampak di kejauhan garis Pantai Palangpang. Masuk ke sini gratis, hanya bayar parkir mobil atau motor. Mobil Rp 20.000.</p>
