<p align="justify">Pantai karang hawu merupakan salah satu pantai yang paling sering dikunjungi pengunjung, lokasinya yang berada dekat dengan jalan utama yaitu jalan Bayan-Cisolok yang menghubungkan kawasan Sukabumi dengan Banten  membuat semua mata yang lewat akan terpana. Pantai ini sudah dikelola dengan cukup baik, terdapat terotoar yang bisa digunakan untuk berjalan kaki, gazebo untuk beristirahat dan juga tempat duduk yang bisa digunakan untuk menikmati keindahan pantai. Letak pantai ini sekitar16 km atau 18 menit dari Pasar Ikan Pelabuhan Ratu.

Panjang garis pantai karang hawu ini mencapai 4 km dengan dua sisi yang berbeda. Sebelah kiri pantai dihiasi dengan bebatuan karang yang terhempas ombak hingga bukit karang. Sedangkan di sisi satunya lagi terdapat hamparan pasir putih kecoklatan yang membentang.</p> 
